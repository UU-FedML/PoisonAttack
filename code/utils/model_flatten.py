import torch

#***********************************************************************************************#
#                                                                                               #
#   Description:                                                                                #
#   flatten the passed model to be used for synchronization.                                    #
#                                                                                               #
#***********************************************************************************************#
def flatten(model):
    vec = []
    for param in model.parameters():
        vec.append(param.data.view(-1))
    return torch.cat(vec)
